/**
 * Generate comprehensive keywords for maximum SEO coverage
 */
export function generateUltraKeywords(): string {
  const allKeywords = ULTRA_KEYWORD_DATABASE.flatMap(category => category.keywords);
  return allKeywords.join(', ');
}

/**
 * Generate keywords by category
 */
export function generateKeywordsByCategory(categoryName: string): string {
  const category = ULTRA_KEYWORD_DATABASE.find(cat => cat.category === categoryName);
  return category ? category.keywords.join(', ') : '';
}

/**
 * Generate high-priority keywords only
 */
export function generateHighPriorityKeywords(): string {
  const highPriorityKeywords = ULTRA_KEYWORD_DATABASE
    .filter(category => category.priority >= 0.9)
    .flatMap(category => category.keywords);
  return highPriorityKeywords.join(', ');
}

/**
 * Get total keyword count
 */
export function getTotalKeywordCount(): number {
  return ULTRA_KEYWORD_DATABASE.reduce((total, category) => total + category.keywords.length, 0);
}

/**
 * Get keyword statistics
 */
export function getKeywordStats() {
  const totalKeywords = getTotalKeywordCount();
  const categories = ULTRA_KEYWORD_DATABASE.length;
  const highPriorityCount = ULTRA_KEYWORD_DATABASE
    .filter(category => category.priority >= 0.9)
    .reduce((total, category) => total + category.keywords.length, 0);
  
  return {
    totalKeywords,
    categories,
    highPriorityCount,
    averageKeywordsPerCategory: Math.round(totalKeywords / categories)
  };
}
